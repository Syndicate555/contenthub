// Get platform info from source domain
export function getPlatformInfo(source: string) {
  const normalizedSource = source?.toLowerCase() || "";

  // Check each platform
  for (const [domain, info] of Object.entries(platforms)) {
    if (normalizedSource.includes(domain.replace("www.", ""))) {
      return { ...info, domain };
    }
  }

  // Default fallback
  return {
    name: source || "Website",
    color: "#6B7280",
    textClassName: "text-gray-500 dark:text-gray-300",
    bgColor: "bg-gray-100 dark:bg-white/10",
    borderColor: "border-l-gray-400 dark:border-l-gray-600",
    domain: "generic",
  };
}

export function PlatformIcon({
  source,
  size = "md",
  showLabel = false,
}: PlatformIconProps) {
  const platform = getPlatformInfo(source);
  const sizeClass = sizeClasses[size];

  const iconComponents: Record<
    string,
    React.ComponentType<{ className?: string }>
  > = {
    "twitter.com": TwitterIcon,
    "x.com": TwitterIcon,
    "linkedin.com": LinkedInIcon,
    "instagram.com": InstagramIcon,
    "youtube.com": YouTubeIcon,
    "github.com": GitHubIcon,
    "reddit.com": RedditIcon,
    "tiktok.com": TikTokIcon,
    "facebook.com": FacebookIcon,
    "medium.com": MediumIcon,
  };

  const IconComponent = iconComponents[platform.domain] || null;

  return (
    <div className="flex items-center gap-1.5">
      <div
        className={`flex items-center justify-center rounded p-1 ${platform.bgColor} ${platform.textClassName}`}
      >
        {IconComponent ? (
          <IconComponent className={sizeClass} />
        ) : (
          <Globe className={sizeClass} />
        )}
      </div>
      {showLabel && (
        <span className={`text-xs font-medium ${platform.textClassName}`}>
          {platform.name}
        </span>
      )}
    </div>
  );
}

export default PlatformIcon;
